#While Loop - Initialization-Condition-Increment/Decrement
i=1
if(i<=10):
    print(i)
    i+=1
    
n=1   #initialization
while(n<=10):   #Condition
    print(n)
    n+=1    #Increment
    
#continue loop on the same line with user input
a=int(input("Enter the limit"))
b=1     #initialization
while(b<=a):
    print(b, end=" ")
    b+=1
