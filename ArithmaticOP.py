# +, -, /, //, %, **
# (+) will do both Addition & Concatenation

#Addition
print("Addition two number")
num1 = int(input("Enter First Number "))
num2 = int(input("Enter Second Number "))
add = num1 + num2
print("Addition is: ",add)
 
#Concatenation
print("Concatenation two number")
sen_1 = input("Enter First No ")
sen_2 = input("Enter Second No ")
print(sen_1+sen_2)

#(-) use for substraction
print("Substraction two number")
num3 = int(input("Enter First Number "))
num4 = int(input("Enter Second Number "))
sub = num3-num4
print("Substraction is: ",sub)

#(*) will do more things like-
#Multiplication
print("Multiplication two number")
num5 = int(input("Enter First Number "))
num6 = int(input("Enter Second Number "))
mul = num5*num6
print("Multiplication is: ",mul)

#(/) use for Division
#always get the float value(7.5)
print("print Division of two number")
print(15/2)

#Print a Word in Multiple Line
print("Print a Word in Multiple Line")
print("Hello"*3)

#(**) use for to calculate power of number
print("print power of numbers")
print(5**3)
print(5**2)

#(%) Modulus operator to get the remainder 
print("print remainder")
print(15%2) #o/p-1

#(//) Floor Division Operator
#To get the integer value from float value
print("Get integer value using floor dividion operator")
print(15//2) #o/p-7
